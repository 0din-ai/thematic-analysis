@main.command()
@click.option(
    "--config",
    "config_path",
    type=click.Path(exists=True, path_type=Path),
    default=None,
    help="Path to TOML config file (auto-detected if omitted).",
)
@click.option(
    "--output-dir",
    type=click.Path(path_type=Path),
    default=None,
    help="Output directory (default: output/ next to config).",
)
@click.option(
    "--resume/--no-resume",
    default=True,
    help="Resume from last completed stage (default: resume).",
)
@click.option(
    "--model",
    default=None,
    help="Override model from config.",
)
@click.option(
    "--reasoning-effort",
    default=None,
    type=click.Choice(
        ["none", "minimal", "low", "medium", "high", "xhigh"],
        case_sensitive=False,
    ),
    help="Override reasoning effort from config.",
)
@click.option(
    "--dry-run",
    is_flag=True,
    default=False,
    help="Show what would happen without making API calls.",
)
def run(
    config_path: Path | None,
    output_dir: Path | None,
    resume: bool,
    model: str | None,
    reasoning_effort: str | None,
    dry_run: bool,
) -> None:
    """Run thematic analysis on interview transcripts."""
    # Find config
    if config_path is None:
        config_path = find_config()
        if config_path is None:
            console.print(
                "[red]Error:[/red] No config.toml found in current directory.\n"
                "Either pass [bold]--config path/to/config.toml[/bold] or run "
                "[bold]thematic-analysis init[/bold] to create a project."
            )
            sys.exit(1)
        console.print(f"[dim]Using config: {config_path}[/dim]")

    # Only require API key for actual runs
    api_key = "" if dry_run else get_api_key()

    config_path = config_path.resolve()
    config = load_config(config_path)

    # CLI overrides
    if model:
        config.model = model
    if reasoning_effort:
        config.reasoning_effort = reasoning_effort

    # Validate prompts file
    if not config.prompts_file.exists():
        console.print(
            f"[red]Error:[/red] Prompts file not found: {config.prompts_file}\n"
            f"Remove the prompts_file setting from your config to use the "
            f"built-in default prompts."
        )
        sys.exit(1)

    # Parse prompts and build steps
    stages = parse_prompts(config.prompts_file)
    steps = build_steps(stages, config)

    if not steps:
        console.print("[red]Error:[/red] No stages parsed from prompts file.")
        sys.exit(1)

    # Set up output directory
    if output_dir is None:
        output_dir = config_path.parent / "output"
    output_dir = output_dir.resolve()

    # Print config summary
    console.print(
        Panel(
            f"[bold]Model:[/bold] {config.model}\n"
            f"[bold]Reasoning:[/bold] {config.reasoning_effort or 'default'}\n"
            f"[bold]Moderator:[/bold] {config.moderator_name}\n"
            f"[bold]Transcripts:[/bold] {len(config.transcripts)}\n"
            f"[bold]Stages:[/bold] {len(steps)} steps\n"
            f"[bold]Output:[/bold] {output_dir}",
            title="Thematic Analysis",
            border_style="cyan",
        )
    )

    if dry_run:
        console.print("\n[bold cyan]Dry run[/bold cyan] — no API calls will be made.")
        console.print("\n[bold]Steps:[/bold]")
        for i, step in enumerate(steps):
            console.print(
                f"  {i + 1}. {step.file_prefix}: {step.title} "
                f"→ {step.filename}"
            )
        console.print("\n[bold]Transcripts:[/bold]")
        for t in config.transcripts:
            exists = "ok" if t["path"].exists() else "[red]NOT FOUND[/red]"
            console.print(f"  {t['participant']}: {t['path']} ({exists})")
        return

    output_dir.mkdir(parents=True, exist_ok=True)

    # Resume logic
    start_index = 0
    messages: list[dict] = []

    if resume:
        resume_point = determine_resume_point(output_dir)
        if resume_point > 0:
            existing_messages = load_conversation(output_dir)
            if existing_messages:
                console.print(
                    f"\n[cyan]Found progress:[/cyan] {resume_point} of "
                    f"{len(steps)} steps complete."
                )
                console.print(
                    "[dim]Press Enter to resume, or 'n' to start fresh[/dim]"
                )
                choice = input("> ").strip().lower()
                if choice != "n":
                    start_index = resume_point
                    messages = existing_messages
                    console.print(
                        f"[green]Resuming from step {start_index + 1}[/green]"
                    )
                else:
                    console.print("[yellow]Starting fresh.[/yellow]")

    if start_index >= len(steps):
        console.print(
            "[green]All steps already complete![/green] "
            f"Final report: {output_dir / steps[-1].filename}"
        )
        return

    # Initialize OpenAI client
    client = OpenAI(api_key=api_key, max_retries=3)

    # Run each step
    for idx in range(start_index, len(steps)):
        step = steps[idx]
        step_label = f"Step {idx + 1}/{len(steps)}"
        stage_label = f"Stage {step.file_prefix}: {step.title}"

        console.print()
        console.rule(f"[bold cyan]{step_label} — {stage_label}[/bold cyan]")
        console.print()

        while True:
            user_msg = {"role": "user", "content": step.prompt}
            current_messages = messages + [user_msg]

            try:
                response_text = send_message(
                    client, current_messages, config.model, config.reasoning_effort
                )
            except KeyboardInterrupt:
                console.print("\n[yellow]Interrupted.[/yellow]")
                save_conversation(output_dir, messages)
                console.print(f"[dim]Progress saved to {output_dir}[/dim]")
                sys.exit(0)

            action = review_prompt()

            if action == "retry":
                console.print("[yellow]Retrying...[/yellow]\n")
                continue
            elif action == "quit":
                save_conversation(output_dir, messages)
                console.print(
                    "[dim]Progress saved. Resume with the same command.[/dim]"
                )
                sys.exit(0)

            messages.append(user_msg)
            messages.append({"role": "assistant", "content": response_text})

            filepath = save_stage_output(output_dir, step, response_text)
            save_conversation(output_dir, messages)
            console.print(f"[green]Saved:[/green] {filepath}")
            break

    console.print()
    console.print(
        Panel(
            "[bold green]Analysis complete![/bold green]\n\n"
            f"Final report: {output_dir / steps[-1].filename}\n"
            f"All outputs: {output_dir}",
            border_style="green",
        )
    )


