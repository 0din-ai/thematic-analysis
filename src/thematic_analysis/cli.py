def _download_google_doc(url: str) -> str:
    """Download a Google Doc as plain text via the public export URL.

    Works for documents shared with 'anyone with the link can view'.
    """
    # Extract document ID from various Google Docs URL formats
    match = re.search(r"/document/d/([a-zA-Z0-9_-]+)", url)
    if not match:
        console.print(
            "[red]Error:[/red] Could not extract document ID from URL.\n"
            "Expected a URL like: "
            "https://docs.google.com/document/d/YOUR_DOC_ID/edit"
        )
        sys.exit(1)

    doc_id = match.group(1)
    export_url = (
        f"https://docs.google.com/document/d/{doc_id}/export?format=txt"
    )

    try:
        req = urllib.request.Request(export_url)
        with urllib.request.urlopen(req, timeout=30) as resp:
            return resp.read().decode("utf-8")
    except urllib.error.HTTPError as e:
        if e.code == 404:
            console.print(
                "[red]Error:[/red] Document not found. Make sure the Google "
                "Doc is shared with 'Anyone with the link'."
            )
        else:
            console.print(f"[red]Error:[/red] HTTP {e.code} downloading document.")
        sys.exit(1)
    except urllib.error.URLError as e:
        console.print(f"[red]Error:[/red] Network error: {e.reason}")
        sys.exit(1)


@main.command("import-transcript")
@click.argument("source")
@click.option(
    "--participant",
    required=True,
    help="Name of the interview participant.",
)
@click.option(
    "--config",
    "config_path",
    type=click.Path(path_type=Path),
    default=None,
    help="Path to config.toml (auto-detected if omitted).",
)
def import_transcript(source: str, participant: str, config_path: Path | None) -> None:
    """Import a transcript from a file or Google Docs URL.

    SOURCE can be a local file path or a Google Docs URL.
    The transcript is copied into the project's transcripts/ directory
    and added to config.toml.
    """
    # Find config
    if config_path is None:
        config_path = find_config()
        if config_path is None:
            console.print(
                "[red]Error:[/red] No config.toml found. Pass --config or "
                "run from a project directory."
            )
            sys.exit(1)

    config_dir = config_path.parent
    transcripts_dir = config_dir / "transcripts"
    transcripts_dir.mkdir(exist_ok=True)

    # Determine if source is a Google Docs URL or a file
    if source.startswith("https://docs.google.com/"):
        console.print(f"[dim]Downloading from Google Docs...[/dim]")
        content = _download_google_doc(source)
        # Generate filename from participant name
        safe_name = re.sub(r"[^\w\s-]", "", participant.lower())
        safe_name = re.sub(r"[\s]+", "-", safe_name)
        filename = f"{safe_name}-transcript.txt"
        dest = transcripts_dir / filename
        dest.write_text(content, encoding="utf-8")
        console.print(f"[green]Downloaded:[/green] {dest}")
    else:
        src = Path(source).expanduser().resolve()
        if not src.exists():
            console.print(f"[red]Error:[/red] File not found: {src}")
            sys.exit(1)
        dest = transcripts_dir / src.name
        shutil.copy2(src, dest)
        console.print(f"[green]Copied:[/green] {dest}")

    # Append to config.toml
    rel_path = f"./transcripts/{dest.name}"
    entry = f'\n[[transcripts]]\npath = "{rel_path}"\nparticipant = "{participant}"\n'
    with open(config_path, "a", encoding="utf-8") as f:
        f.write(entry)

    console.print(
        f"[green]Added to config:[/green] {participant} → {rel_path}"
    )
