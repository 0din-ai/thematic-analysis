def _multiline_input(prompt_text: str, example: str) -> str:
    """Prompt for multiline input with an example shown."""
    console.print(
        Panel(
            f"[dim]Example:[/dim]\n{example}",
            border_style="dim",
        )
    )
    console.print(f"[bold]{prompt_text}[/bold]")
    console.print("[dim]Type or paste your text. Enter a blank line when done.[/dim]")
    lines = []
    while True:
        try:
            line = input()
        except EOFError:
            break
        if line == "" and lines:
            break
        lines.append(line)
    return "\n".join(lines)


@main.command()
@click.argument("name", default="")
def init(name: str) -> None:
    """Create a new analysis project interactively."""
    console.print(
        Panel(
            "[bold]Thematic Analysis — New Project Setup[/bold]\n\n"
            "This will walk you through creating a new analysis project.\n"
            "You can always edit the config.toml file later.",
            border_style="cyan",
        )
    )

    # Project name
    if not name:
        name = Prompt.ask(
            "Project name",
            default="interview-analysis",
        )

    project_dir = Path.cwd() / "projects" / name
    if project_dir.exists():
        if not Confirm.ask(
            f"Directory [bold]{project_dir}[/bold] already exists. Overwrite config?"
        ):
            console.print("[yellow]Cancelled.[/yellow]")
            return

    # Moderator name
    moderator_name = Prompt.ask(
        "Your name (as the interviewer/moderator)",
        default="",
    )
    if not moderator_name:
        console.print(
            "[yellow]Warning:[/yellow] No moderator name set. "
            "The AI may confuse moderator and participant quotes."
        )

    # Research questions
    research_questions = _multiline_input(
        "Enter your research questions (one per line):",
        "1. What gaps exist in the AI security market?\n"
        "2. How are product teams currently approaching AI security?\n"
        "3. What friction points exist between security and product velocity?",
    )

    # Business context
    business_context = _multiline_input(
        "Describe the business context for this research:",
        "We are exploring the AI security market to identify gaps\n"
        "that can be filled with new products. The focus is on\n"
        "understanding how product teams handle security concerns.",
    )

    # Model
    model = Prompt.ask(
        "OpenAI model",
        default="gpt-5.4",
        choices=["gpt-4o", "gpt-5", "gpt-5.4", "gpt-5.5"],
    )

    # Reasoning effort
    reasoning_effort = Prompt.ask(
        "Reasoning effort",
        default="high",
        choices=["none", "minimal", "low", "medium", "high", "xhigh"],
    )

    # Transcript (optional)
    transcript_path = Prompt.ask(
        "Path to transcript file (or press Enter to skip)",
        default="",
    )
    participant = ""
    if transcript_path:
        participant = Prompt.ask("Participant name")

    # Create project directory
    project_dir.mkdir(parents=True, exist_ok=True)
    (project_dir / "transcripts").mkdir(exist_ok=True)
    (project_dir / "output").mkdir(exist_ok=True)

    # Generate config
    config_content = CONFIG_TEMPLATE.format(
        model=model,
        reasoning_effort=reasoning_effort,
        moderator_name=moderator_name,
        research_questions=research_questions,
        business_context=business_context,
    )

    # Add transcript entry if provided
    if transcript_path:
        src = Path(transcript_path).expanduser().resolve()
        if src.exists():
            dest = project_dir / "transcripts" / src.name
            shutil.copy2(src, dest)
            rel_path = f"./transcripts/{src.name}"
            console.print(f"[green]Copied transcript to:[/green] {dest}")
        else:
            rel_path = transcript_path
            console.print(
                f"[yellow]Warning:[/yellow] File not found: {src}. "
                f"Path saved as-is in config."
            )
        config_content += f'\n[[transcripts]]\npath = "{rel_path}"\n'
        config_content += f'participant = "{participant}"\n'

    config_file = project_dir / "config.toml"
    config_file.write_text(config_content, encoding="utf-8")

    # Print summary
    console.print()
    console.print(
        Panel(
            f"[bold green]Project created![/bold green]\n\n"
            f"  Directory: {project_dir}\n"
            f"  Config:    {config_file}\n\n"
            f"[bold]Next steps:[/bold]\n"
            f"  1. Review/edit the config:  [cyan]{config_file}[/cyan]\n"
            f"  2. Add transcripts:         "
            f"[cyan]thematic-analysis import-transcript <file> "
            f"--participant \"Name\"[/cyan]\n"
            f"  3. Run the analysis:        "
            f"[cyan]cd {project_dir} && thematic-analysis run[/cyan]",
            border_style="green",
        )
    )


