def review_prompt() -> str:
    """Ask the user what to do after viewing a response.

    Returns one of: 'continue', 'retry', 'quit'.
    """
    console.print()
    console.print(
        "[dim]Press [bold]Enter[/bold] to continue, "
        "[bold]r[/bold] to retry, "
        "[bold]q[/bold] to quit[/dim]"
    )
    choice = input("> ").strip().lower()
    if choice == "r":
        return "retry"
    elif choice == "q":
        return "quit"
    return "continue"


